public class operators {
    public static void main(String[] args) {
        System.out.println(3 >= 3 && 4 < 2);

        /* && (AND)
         * This operator checks whether both of the conditions are true.
         * if any of them is false then it will return false else true. 
        */

        System.out.println(3 < 3 || 4 < 2);

        /* || (OR)
         * This operator checks whether any of the conditions are true.
         * if any of them is true then it will return true else false. 
        */
    }
}
